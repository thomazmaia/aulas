# Parâmetros de funções:
# - São variáveis que são declaradas na definição de uma função. Eles são utilizados para receber valores quando a função é chamada. Os parâmetros servem como "espaços reservados" para os valores que serão passados para a função quando ela for executada. Em essência, os parâmetros definem quais informações a função espera para realizar sua tarefa.

# Argumentos de funções:
# - São os valores reais que são passados para a função quando ela é chamada. Os argumentos são fornecidos na chamada da função e preenchem os parâmetros declarados na definição da função. Os argumentos são os dados com os quais a função realmente trabalha durante sua execução.


def saudacao(param):
    print(f"Olá, {param}!")

saudacao("Maria")
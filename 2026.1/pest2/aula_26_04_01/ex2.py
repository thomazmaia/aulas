# Crie uma função para receber a BASE e a ALTURA de um retângulo e imprima a ÁREA desse retângulo.
# Ex: (base = 10; altura = 20)
#.    Área = 200

def calcular_area(base, altura):
    area = base * altura
    print(f"Área = {area}")

calcular_area(10, 20)
calcular_area(3, 45)
calcular_area(7.35, 2)
calcular_area(1.5, 10)
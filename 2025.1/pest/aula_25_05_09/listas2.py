# MODIFICANDO ELEMENTOS DE UMA LISTA:
# Podemos modificar os elementos de uma lista utilizando o índice do elemento que desejamos alterar.
# Exemplo de modificação de elementos da lista
lista = [1, 2, 3, 4, 5]
print(lista)

lista[0] = 'a'  # Altera o primeiro elemento da lista para 'a'
print(lista)

lista[4] = 'z' # Altera o quinto elemento da lista para 'z'
print(lista)
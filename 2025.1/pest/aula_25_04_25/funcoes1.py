# FUNÇÕES: São blocos de código que podem ser reutilizados em diferentes partes do programa.
#
# Sintaxe básica:
# def nome_da_funcao(parametros):
#     # bloco de código

def saudacao(nome):
    print(f"Olá, {nome}! Bem-vindo(a) à aula de funções.")


saudacao("João") # "João" é o argumento passado para a função
saudacao("Maria") # "Maria" é o argumento passado para a função
saudacao("Ana") # "Ana" é o argumento passado para a função
# A função saudacao é chamada três vezes com diferentes argumentos, e cada vez ela imprime uma mensagem personalizada.
# A função saudacao recebe um parâmetro chamado nome e imprime uma mensagem de saudação personalizada.

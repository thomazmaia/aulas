# Crie uma função que receba dois parâmetros: a altura e a largura de um retângulo. Sua função deve calcular e imprimir a área desse retângulo.
# Teste a função.


def calc_area_retangulo(altura, largura):
    area = altura * largura
    print(f"{area} metros quadrados")

calc_area_retangulo(2, 5)
calc_area_retangulo(3.5, 12.54)
calc_area_retangulo(40, 25)
calc_area_retangulo(37.2, 98.9)
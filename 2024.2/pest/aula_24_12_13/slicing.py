# FATIAMENTO (SLICING) DE STRINGS
# Ex: como separar uma frase em palavras (substrings)
#     str = "Hoje é um bom dia"
#     substring = str[0:4]

str = "Hoje é um bom dia"
substr1 = str[0:4] # Hoje
substr2 = str[5] # é
substr3 = str[7:9] # um
substr4 = str[10:13] # bom
substr5 = str[14:17] # dia


print(substr1)
print(substr2)
print(substr3)
print(substr4)
print(substr5)
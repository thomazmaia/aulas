# Escreva um programa que cria um dicionário de produtos e preços. O programa deve permitir ao usuário adicionar novos produtos e preços, atualizar preços existentes e excluir produtos do dicionário. Cada produto deve estar relacionado a uma categoria (Bebida, Comida, Limpeza e Frutas).
# Ex:
produtos = {
    'Coca-Cola': {'categoria': 'Bebida', 'preco': 4.5},
    'Pizza': {'categoria': 'Comida', 'preco': 35},
    'Detergente': {'categoria': 'Limpeza', 'preco': 2},
    'Laranja': {'categoria': 'Frutas', 'preco': 5.2}
}
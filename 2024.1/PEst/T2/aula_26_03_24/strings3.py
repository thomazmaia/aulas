# Operações com strings

# Concatenação ("soma") de strings
str1 = "Olá"
str2 = " "
str3 = "amigo"

res = str1 + str2 + str3
print(res)

# Repetição ("multiplicação") de strings
str4 = "Olê "
res = 4 * str4
print(res)


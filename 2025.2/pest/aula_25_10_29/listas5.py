# Listas aninhadas
# Listas podem conter outras listas como seus elementos, criando estruturas de dados mais complexas: listas de listas (matrizes)

M = [['a', 'b', 'c'], 
     ['d', 'e', 'f'], 
     ['g', 'h', 'i']]

print(M[1][2])

L = [[1, 2, 3], 
     ['a', 'b'], 
     ['uva', 1.5, 10, -99]]

print(L[2][1])